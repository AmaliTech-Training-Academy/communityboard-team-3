"""Shared database helpers, logging, and schema validation for data-engineering scripts."""
import logging
import os
import sys
from pathlib import Path

from sqlalchemy import create_engine, text

from config import DATABASE_URL, ANALYTICS_DATABASE_URL

_engine = None
_analytics_engine = None

# Log files go into data-engineering/logs/<script_name>.log
LOGS_DIR = Path(__file__).parent / "logs"


def setup_logging(name: str) -> logging.Logger:
    """Return a logger that writes to both stdout and logs/<name>.log."""
    logger = logging.getLogger(name)
    if not logger.handlers:
        fmt = logging.Formatter(
            "%(asctime)s [%(levelname)s] %(name)s: %(message)s",
            datefmt="%Y-%m-%d %H:%M:%S",
        )

        # Console handler
        console = logging.StreamHandler(sys.stdout)
        console.setFormatter(fmt)
        logger.addHandler(console)

        # File handler — one file per script in logs/
        LOGS_DIR.mkdir(exist_ok=True)
        # Use the last part of the dotted module name as the filename
        log_filename = name.rsplit(".", 1)[-1] if "." in name else name
        file_handler = logging.FileHandler(
            LOGS_DIR / f"{log_filename}.log", encoding="utf-8"
        )
        file_handler.setFormatter(fmt)
        logger.addHandler(file_handler)

        logger.setLevel(logging.INFO)
    return logger


def get_engine():
    """Return a singleton SQLAlchemy engine for the source database."""
    global _engine
    if _engine is None:
        _engine = create_engine(DATABASE_URL, pool_pre_ping=True)
    return _engine


def get_analytics_engine():
    """Return a singleton SQLAlchemy engine for the analytics database."""
    global _analytics_engine
    if _analytics_engine is None:
        _analytics_engine = create_engine(ANALYTICS_DATABASE_URL, pool_pre_ping=True)
    return _analytics_engine



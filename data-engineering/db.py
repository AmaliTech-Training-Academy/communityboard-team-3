def ensure_schema(conn, logger=None):
    """Create tables if missing; log column mismatches for existing tables.

    Call this before any seed or ETL work to guarantee the schema is ready.
    Tables are created with IF NOT EXISTS so this is safe to run repeatedly.
    """
    _log = logger or logging.getLogger(__name__)

    for table_name, spec in EXPECTED_SCHEMA.items():
        # Check if table exists
        exists = conn.execute(text(
            "SELECT EXISTS ("
            "  SELECT 1 FROM information_schema.tables "
            "  WHERE table_schema = 'public' AND table_name = :t"
            ")"
        ), {"t": table_name}).scalar()

        if not exists:
            _log.warning("Table '%s' does not exist — creating it now", table_name)
            conn.execute(text(spec["ddl"]))
            conn.commit()
            _log.info("Table '%s' created successfully", table_name)
        else:
            # Table exists — verify columns match expectations
            rows = conn.execute(text(
                "SELECT column_name FROM information_schema.columns "
                "WHERE table_schema = 'public' AND table_name = :t"
            ), {"t": table_name}).fetchall()
            actual_columns = {row[0] for row in rows}
            expected_columns = spec["columns"]

            missing = expected_columns - actual_columns
            extra = actual_columns - expected_columns

            if missing:
                _log.warning(
                    "Table '%s' is MISSING expected columns: %s",
                    table_name, ", ".join(sorted(missing)),
                )
            if extra:
                _log.info(
                    "Table '%s' has extra columns (added by backend?): %s",
                    table_name, ", ".join(sorted(extra)),
                )
            if not missing and not extra:
                _log.info("Table '%s' schema OK (%d columns)", table_name, len(actual_columns))
